const i18nNodes = [...document.querySelectorAll("[data-i18n]")];
const placeholderNodes = [...document.querySelectorAll("[data-i18n-placeholder]")];
i18nNodes.forEach((node) => { node.dataset.defaultText = node.innerHTML; });
placeholderNodes.forEach((node) => { node.dataset.defaultPlaceholder = node.placeholder; });

function setLanguage(language) {
  const dictionary = translations[language];
  document.documentElement.lang = language;
  i18nNodes.forEach((node) => {
    const key = node.dataset.i18n;
    node.innerHTML = dictionary?.[key] ?? node.dataset.defaultText;
  });
  placeholderNodes.forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    node.placeholder = dictionary?.[key] ?? node.dataset.defaultPlaceholder;
  });
  localStorage.setItem("ai-academy-language", language);
}

const languageSelect = document.querySelector("#language-select");
const savedLanguage = localStorage.getItem("ai-academy-language") || "en";
languageSelect.value = savedLanguage;
setLanguage(savedLanguage);
languageSelect.addEventListener("change", (event) => setLanguage(event.target.value));

const menuButton = document.querySelector(".menu-toggle");
const primaryNav = document.querySelector("#primary-nav");
menuButton.addEventListener("click", () => {
  const open = menuButton.getAttribute("aria-expanded") === "true";
  menuButton.setAttribute("aria-expanded", String(!open));
  primaryNav.classList.toggle("open", !open);
});
primaryNav.querySelectorAll("a").forEach((link) => link.addEventListener("click", () => {
  menuButton.setAttribute("aria-expanded", "false");
  primaryNav.classList.remove("open");
}));

const triggers = [...document.querySelectorAll(".card-trigger")];
function setCardOpen(trigger, shouldOpen) {
  const panel = document.getElementById(trigger.getAttribute("aria-controls"));
  trigger.setAttribute("aria-expanded", String(shouldOpen));
  trigger.closest(".resource-card").classList.toggle("is-open", shouldOpen);
  panel.hidden = !shouldOpen;
}
triggers.forEach((trigger) => trigger.addEventListener("click", () => {
  const opening = trigger.getAttribute("aria-expanded") !== "true";
  triggers.forEach((item) => { if (item !== trigger) setCardOpen(item, false); });
  setCardOpen(trigger, opening);
}));

const searchInput = document.querySelector("#resource-search");
const searchStatus = document.querySelector("#search-status");
const resourceCards = [...document.querySelectorAll(".resource-card")];
const emptyState = document.querySelector("#empty-state");
function runSearch() {
  const query = searchInput.value.trim().toLocaleLowerCase();
  let visible = 0;
  resourceCards.forEach((card) => {
    const haystack = `${card.dataset.search} ${card.textContent}`.toLocaleLowerCase();
    const match = !query || haystack.includes(query);
    card.hidden = !match;
    if (match) visible += 1;
  });
  emptyState.hidden = visible !== 0;
  searchStatus.textContent = query ? `${visible} ${visible === 1 ? "category" : "categories"} found` : "";
  if (query) document.querySelector("#resources").scrollIntoView({ behavior: "smooth", block: "start" });
}
searchInput.addEventListener("input", runSearch);
document.querySelector("#global-search").addEventListener("submit", (event) => { event.preventDefault(); runSearch(); });
document.addEventListener("keydown", (event) => {
  if ((event.metaKey || event.ctrlKey) && event.key.toLowerCase() === "k") { event.preventDefault(); searchInput.focus(); }
  if (event.key === "Escape" && document.activeElement === searchInput) { searchInput.value = ""; runSearch(); searchInput.blur(); }
});

document.querySelectorAll("[data-open]").forEach((button) => button.addEventListener("click", () => {
  const trigger = document.querySelector(`[aria-controls="${button.dataset.open}"]`);
  resourceCards.forEach((card) => { card.hidden = false; });
  searchInput.value = "";
  searchStatus.textContent = "";
  setCardOpen(trigger, true);
  trigger.closest(".resource-card").scrollIntoView({ behavior: "smooth", block: "center" });
}));

